export function resolveCategoryKey(raw: string): string | null {
  const key = raw.replace(/_/g, '-');
  if (CATEGORY_SEO[key]) return key;
  // try aliases
  for (const [k, v] of Object.entries(CATEGORY_SEO)) {
    if (v.alias?.includes(key)) return k;
  }
  return null;
}
